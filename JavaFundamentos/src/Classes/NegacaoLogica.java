package Classes;

public class NegacaoLogica {
    public static void main(String[] args) {
        // Exemplo de uso do operador `!` (NÃO lógico) em Java

        // Definindo uma variável booleana
        boolean expressaoVerdadeira = true;

        // Aplicando a negação lógica com `!`
        boolean expressaoFalsa = !expressaoVerdadeira;

        // Imprimindo os resultados
        System.out.println("Expressão original: " + expressaoVerdadeira);  // Saída: true
        System.out.println("Expressão após negação: " + expressaoFalsa);  // Saída: false
    }
}
